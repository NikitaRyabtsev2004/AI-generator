def pick_triplet(aspects: list[str], seed: int) -> tuple[str, str, str]:
    idx = seed % len(aspects)
    return aspects[idx], aspects[(idx + 1) % len(aspects)], aspects[(idx + 2) % len(aspects)]


def build_question(title: str, intent: str, a1: str, a2: str, rng: random.Random) -> str:
    return rng.choice(QUESTION_VARIANTS[intent]).format(title=title, a1=a1, a2=a2)


def build_answer(title: str, focus: str, intent: str, a1: str, a2: str, a3: str, rng: random.Random) -> str:
    base = [rng.choice(OPENERS), rng.choice(FOCUS).format(title=title, focus=focus)]
    if intent == "start":
        body = [rng.choice(FOUNDATION).format(a1=a1, a2=a2), rng.choice(ACTION).format(a1=a1, a2=a2), f"На этом этапе направление «{a3}» полезно держать в поле зрения, но не превращать его в отдельный сложный проект раньше времени."]
    elif intent == "priority":
        body = [f"Если приходится выбирать между блоком «{a1}» и блоком «{a2}», сначала укрепляй то, без чего нельзя спокойно оценить остальную работу.", rng.choice(COMPARE), f"После стабилизации блока «{a1}» обычно легче увидеть реальную роль блока «{a2}», а не додумывать ее на глаз."]
    elif intent == "mistakes":
        body = [rng.choice(MISTAKE).format(a1=a1, a2=a2), f"Если рядом есть направление «{a3}», его лучше использовать как контрольную точку, а не как повод усложнить все сразу.", rng.choice(DIAGNOSE).format(a1=a1, a2=a2)]
    elif intent == "routine":
        body = [rng.choice(ROUTINE).format(a1=a1, a2=a2), f"Я бы выделил небольшой постоянный слот, в котором ты сначала смотришь на блок «{a1}», затем на блок «{a2}», а уже потом замечаешь, что происходит с направлением «{a3}».", rng.choice(PROGRESS).format(a1=a1, a2=a2)]
    elif intent == "progress":
        body = [rng.choice(PROGRESS).format(a1=a1, a2=a2), f"Хорошо, когда по направлению «{a3}» тоже появляется ясность: ты видишь не только результат, но и путь, который к нему привел.", rng.choice(OBSERVE).format(a1=a1, a2=a2)]
    elif intent == "budget":
        body = [rng.choice(BUDGET).format(a1=a1, a2=a2), f"Если уже на минимальной базе можно честно наблюдать блок «{a1}» и спокойно удерживать блок «{a2}», значит старт собран разумно.", rng.choice(ACTION).format(a1=a1, a2=a2)]
    elif intent == "diagnose":
        body = [rng.choice(DIAGNOSE).format(a1=a1, a2=a2), f"После этого полезно проверить, не вмешалось ли в картину направление «{a3}», которое раньше казалось нейтральным.", rng.choice(MISTAKE).format(a1=a1, a2=a2)]
    elif intent == "deepen":
        body = [rng.choice(DEEPEN).format(a1=a1, a2=a2), f"Если база действительно собрана, то направление «{a3}» уже можно использовать не как костыль, а как осмысленное расширение уровня.", rng.choice(PROGRESS).format(a1=a1, a2=a2)]
    elif intent == "observe":
        body = [rng.choice(OBSERVE).format(a1=a1, a2=a2), f"Отдельно полезно замечать, как ведет себя направление «{a3}»: иногда именно оно объясняет, почему связка из блока «{a1}» и блока «{a2}» дает разный результат в разные дни.", rng.choice(FOUNDATION).format(a1=a1, a2=a2)]
    else:
        first = a1 if rng.random() < 0.5 else a2
        second = a2 if first == a1 else a1
        body = [f"Если выбирать только одну первичную опору, я бы сначала стабилизировал блок «{first}», потому что без него остальная часть процесса теряет читаемость.", f"Блок «{second}» важен не меньше, но его проще настраивать уже после того, как блок «{first}» перестал вести себя хаотично.", rng.choice(COMPARE)]
    return " ".join(base + body + [rng.choice(CLOSE)])


def build_file(topic: tuple[str, str, str, list[str]], file_index: int) -> str:
    filename, title, focus, aspects = topic
    lines = ["Набор: broad_unique_dialogues", f"Файл: {file_index}", f"Тема: {title}", ""]
    used_q: set[str] = set()
    used_a: set[str] = set()
    for pair_index in range(PAIR_COUNT):
        intent = INTENTS[pair_index % len(INTENTS)]
        for attempt in range(120):
            rng = random.Random(f"{filename}:{pair_index}:{attempt}")
            a1, a2, a3 = pick_triplet(aspects, pair_index * 2 + attempt)
            q = build_question(title, intent, a1, a2, rng)
            a = build_answer(title, focus, intent, a1, a2, a3, rng)
            if q in used_q or a in used_a:
                continue
            used_q.add(q)
            used_a.add(a)
            lines.append(f"Пользователь: {q}")
            lines.append(f"Ассистент: {a}")
            lines.append("")
            break
        else:
            raise RuntimeError(f'Не удалось собрать уникальную пару для {filename} #{pair_index + 1}')
    return "\n".join(lines).strip() + "\n"


def main() -> None:
    TARGET.mkdir(parents=True, exist_ok=True)
    for index, topic in enumerate(TOPICS, start=1):
        (TARGET / topic[0]).write_text(build_file(topic, index), encoding="utf-8-sig")


if __name__ == "__main__":
    main()
